import time
import traceback
from pathlib import Path
from typing import Callable

from watchdog.events import FileSystemEventHandler
from watchdog.observers import Observer

from src.logger import logger


class FolderWatcher(FileSystemEventHandler):
    """–ö–ª–∞—Å—Å –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ –∑–∞–¥–∞–Ω–Ω–æ–π –ø–∞–ø–∫–µ –∏ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è callback-—Ñ—É–Ω–∫—Ü–∏–∏.

    –≠—Ç–æ—Ç –∫–ª–∞—Å—Å –ø–æ–∑–≤–æ–ª—è–µ—Ç –æ—Ç—Å–ª–µ–∂–∏–≤–∞—Ç—å –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ —Ü–µ–ª–µ–≤–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ (—Å–æ–∑–¥–∞–Ω–∏–µ –∏ –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Ñ–∞–π–ª–æ–≤)
    –∏ –≤—ã–∑—ã–≤–∞—Ç—å –∑–∞–¥–∞–Ω–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é-–æ–±—Ä–∞–±–æ—Ç—á–∏–∫ (callback). –û–±—Ä–∞–±–æ—Ç–∫–∞ –º–æ–∂–µ—Ç –±—ã—Ç—å –∑–∞–ø—É—â–µ–Ω–∞:
    - –ø–æ —Å–æ–±—ã—Ç–∏—è–º —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã, –ø–æ—Å–ª–µ —Ç–æ–≥–æ –∫–∞–∫ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å –ø—Ä–µ–∫—Ä–∞—Ç–∏–ª–∞—Å—å –Ω–∞ –∑–∞–¥–∞–Ω–Ω–æ–µ –≤—Ä–µ–º—è
    - –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ —á–µ—Ä–µ–∑ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–µ –ø—Ä–æ–º–µ–∂—É—Ç–∫–∏ –≤—Ä–µ–º–µ–Ω–∏, –¥–∞–∂–µ –µ—Å–ª–∏ —Å–æ–±—ã—Ç–∏–π –Ω–µ –±—ã–ª–æ
        (–¥–ª—è –ø–æ–≤—ã—à–µ–Ω–∏—è —É—Å—Ç–æ–π—á–∏–≤–æ—Å—Ç–∏)
    """

    def __init__(
            self,
            folder_path: str | Path,
            callback: Callable[[], None],
            forced_timeout: float | int = 300,
            event_delay: float | int = 3
    ):
        """–ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—è –∑–∞ –ø–∞–ø–∫–æ–π.

        Args:
            folder_path: –ü—É—Ç—å –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏, –∑–∞ –∫–æ—Ç–æ—Ä–æ–π –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ —Å–ª–µ–¥–∏—Ç—å
            callback: –§—É–Ω–∫—Ü–∏—è, –≤—ã–∑—ã–≤–∞–µ–º–∞—è –ø—Ä–∏ –æ–±–Ω–∞—Ä—É–∂–µ–Ω–∏–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–π
            forced_timeout: –ü–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –≤—ã–∑–æ–≤–∞ callback, –¥–∞–∂–µ –µ—Å–ª–∏ –Ω–µ—Ç –Ω–æ–≤—ã—Ö —Å–æ–±—ã—Ç–∏–π (–≤ —Å–µ–∫—É–Ω–¥–∞—Ö)
            event_delay: –ó–∞–¥–µ—Ä–∂–∫–∞ –ø–µ—Ä–µ–¥ –≤—ã–∑–æ–≤–æ–º callback –ø–æ—Å–ª–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —Å–æ–±—ã—Ç–∏—è (–≤ —Å–µ–∫—É–Ω–¥–∞—Ö).

                –ü—Ä–∏ –≤–æ–∑–Ω–∏–∫–Ω–æ–≤–µ–Ω–∏–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–π –≤ –ø–∞–ø–∫–µ (—Å–æ–∑–¥–∞–Ω–∏–µ –∏–ª–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Ñ–∞–π–ª–æ–≤) –∑–∞–ø—É—Å–∫ callback
                –Ω–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –Ω–µ–º–µ–¥–ª–µ–Ω–Ω–æ. –í–º–µ—Å—Ç–æ —ç—Ç–æ–≥–æ —Å–∏—Å—Ç–µ–º–∞ –∂–¥—ë—Ç, —á—Ç–æ–±—ã —Å –º–æ–º–µ–Ω—Ç–∞ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —Å–æ–±—ã—Ç–∏—è
                –ø—Ä–æ—à–ª–æ –Ω–µ –º–µ–Ω–µ–µ `event_delay` —Å–µ–∫—É–Ω–¥.

                –≠—Ç–æ –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –ø—Ä–µ–∂–¥–µ–≤—Ä–µ–º–µ–Ω–Ω—É—é –æ–±—Ä–∞–±–æ—Ç–∫—É —Ñ–∞–π–ª–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—â—ë –º–æ–≥—É—Ç –±—ã—Ç—å –Ω–µ –¥–æ –∫–æ–Ω—Ü–∞
                —Å–∫–æ–ø–∏—Ä–æ–≤–∞–Ω—ã, –∑–∞–≥—Ä—É–∂–µ–Ω—ã –∏–ª–∏ —Å–æ–∑–¥–∞–Ω—ã. –ï—Å–ª–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –ø—Ä–æ–¥–æ–ª–∂–∞—é—Ç –ø–æ—è–≤–ª—è—Ç—å—Å—è –Ω–æ–≤—ã–µ —Å–æ–±—ã—Ç–∏—è,
                —Ç–æ –æ—Ç—Å—á—ë—Ç –≤—Ä–µ–º–µ–Ω–∏ –æ—Ç–∫–ª–∞–¥—ã–≤–∞–µ—Ç—Å—è, –∏ callback –±—É–¥–µ—Ç –≤—ã–∑–≤–∞–Ω —Ç–æ–ª—å–∫–æ —Ç–æ–≥–¥–∞, –∫–æ–≥–¥–∞ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å
                –ø—Ä–µ–∫—Ä–∞—Ç–∏—Ç—Å—è –∫–∞–∫ –º–∏–Ω–∏–º—É–º –Ω–∞ `event_delay` —Å–µ–∫—É–Ω–¥.
        """
        self.folder_path: Path = Path(folder_path)
        self.callback: Callable[[], None] = callback
        self.forced_timeout: float = float(forced_timeout)
        self.event_delay: float = float(event_delay)

        # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
        self.event_detected: bool = False  # –§–ª–∞–≥, —É–∫–∞–∑—ã–≤–∞—é—â–∏–π –Ω–∞ –Ω–∞–ª–∏—á–∏–µ –Ω–µ–æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω–æ–≥–æ —Å–æ–±—ã—Ç–∏—è
        self.last_event_time: float = 0.0  # –í—Ä–µ–º—è –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —Å–æ–±—ã—Ç–∏—è
        self.is_processing: bool = False  # –§–ª–∞–≥, —É–∫–∞–∑—ã–≤–∞—é—â–∏–π, –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –ª–∏ –æ–±—Ä–∞–±–æ—Ç–∫–∞ –≤ –¥–∞–Ω–Ω—ã–π –º–æ–º–µ–Ω—Ç
        self.observer: Observer | None = None  # –û–±—ä–µ–∫—Ç –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—è —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã. –ë—É–¥–µ—Ç –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω –≤ –º–µ—Ç–æ–¥–µ monitor

    def on_any_event(self, event) -> None:
        """
        –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –º–µ—Ç–æ–¥ –∫–ª–∞—Å—Å–∞ FileSystemEventHandler.
        –ú–µ—Ç–æ–¥ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã –≤ —Ü–µ–ª–µ–≤–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ (—Å–æ–∑–¥–∞–Ω–∏–µ, –∏–∑–º–µ–Ω–µ–Ω–∏–µ).

        –ò–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç —Å–æ–±—ã—Ç–∏—è —É–¥–∞–ª–µ–Ω–∏—è —Ñ–∞–π–ª–æ–≤ –∏ –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ (—Å —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è–º–∏ .tmp, .part, ~),
        —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –Ω–µ–Ω—É–∂–Ω–æ–π –æ–±—Ä–∞–±–æ—Ç–∫–∏. –ü—Ä–∏ –≤–∞–ª–∏–¥–Ω–æ–º —Å–æ–±—ã—Ç–∏–∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —Ñ–ª–∞–≥ –∏ —Ñ–∏–∫—Å–∏—Ä—É–µ—Ç –≤—Ä–µ–º—è.

        Args:
            event: –°–æ–±—ã—Ç–∏–µ –æ—Ç watchdog, —Å–æ–¥–µ—Ä–∂–∞—â–µ–µ —Ç–∏–ø —Å–æ–±—ã—Ç–∏—è –∏ –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É.
        """
        # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å–æ–±—ã—Ç–∏—è —É–¥–∞–ª–µ–Ω–∏—è –∏ –≤—Ä–µ–º–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã
        ignored_extensions = {".tmp", ".part", "~"}
        if (
                event.event_type == "deleted"
                or any(event.src_path.endswith(ext) for ext in ignored_extensions)
        ):
            return

        logger.debug(f"üìÅ –û–±–Ω–∞—Ä—É–∂–µ–Ω–æ –∏–∑–º–µ–Ω–µ–Ω–∏–µ: {event.src_path} ({event.event_type})")
        # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ñ–ª–∞–≥ —Å–æ–±—ã—Ç–∏—è –∏ —Ñ–∏–∫—Å–∏—Ä—É–µ–º —Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è
        self.event_detected = True
        self.last_event_time = time.time()

    def stop(self) -> None:
        """–û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –∏ –∑–∞–≤–µ—Ä—à–∞–µ—Ç –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—å.

        –ë–µ–∑–æ–ø–∞—Å–Ω–æ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—å —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã, –¥–æ–∂–∏–¥–∞–µ—Ç—Å—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –µ–≥–æ —Ä–∞–±–æ—Ç—ã
        –∏ —Å–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Å–æ—Å—Ç–æ—è–Ω–∏–µ. –ï—Å–ª–∏ –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—å —É–∂–µ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω, –º–µ—Ç–æ–¥ –Ω–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç –¥–µ–π—Å—Ç–≤–∏–π.
        """
        if not self.observer:
            logger.debug("üîî –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –£–ñ–ï –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω")
            return

        try:
            self.observer.stop()
            self.observer.join()
            logger.info("üîî –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω")
        except Exception as e:
            logger.error(f"‚õî –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Å—Ç–∞–Ω–æ–≤–∫–µ –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—è: {e}\n{traceback.format_exc()}")
        finally:
            self.observer = None

    def monitor(self):
        """–û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–π.

        - –ó–∞–ø—É—Å–∫–∞–µ—Ç –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—å –∑–∞ —É–∫–∞–∑–∞–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–µ–π.
        - –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Å–æ–±—ã—Ç–∏—è —Å –∑–∞–¥–µ—Ä–∂–∫–æ–π –∏–ª–∏ —á–µ—Ä–µ–∑ –∑–∞–¥–∞–Ω–Ω—ã–π –ø–µ—Ä–∏–æ–¥.
        - –û–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç—å –ø—Ä–∏ –º–Ω–æ–≥–æ–∫—Ä–∞—Ç–Ω–æ–º —Å—Ä–∞–±–∞—Ç—ã–≤–∞–Ω–∏–∏ —Å–æ–±—ã—Ç–∏–π –∏ –æ—à–∏–±–∫–∞—Ö.

        Raises:
            Exception: –ü—Ä–∏ –∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏—Ö –æ—à–∏–±–∫–∞—Ö –≤–æ –≤—Ä–µ–º—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞.
        """
        # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –∏ –∑–∞–ø—É—Å–∫ –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—è
        self.observer = Observer()
        try:
            # –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—å –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —Å–æ–±—ã—Ç–∏–π –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ (–±–µ–∑ —Ä–µ–∫—É—Ä—Å–∏–∏)
            self.observer.schedule(self, str(self.folder_path), recursive=False)
            self.observer.start()
            logger.info(
                f"üìÅ –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ (–ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω—ã–π —Ç–∞–π–º–∞—É—Ç {self.forced_timeout:.0f} —Å–µ–∫): {self.folder_path}")
        except Exception as e:
            logger.error(f"‚õî –û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞ –Ω–∞–±–ª—é–¥–∞—Ç–µ–ª—è: {e}\n{traceback.format_exc()}")
            self.stop()
            return

        try:
            last_callback_time: float = 0.0
            while True:
                # –ù–µ–±–æ–ª—å—à–∞—è –ø–∞—É–∑–∞ –¥–ª—è –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏—è —á—Ä–µ–∑–º–µ—Ä–Ω–æ–π –Ω–∞–≥—Ä—É–∑–∫–∏ –Ω–∞ CPU
                time.sleep(.1)
                current_time = time.time()

                # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –∏—Ç–µ—Ä–∞—Ü–∏—é, –µ—Å–ª–∏ —É–∂–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è callback
                if self.is_processing:
                    continue

                # –£—Å–ª–æ–≤–∏–µ –∑–∞–ø—É—Å–∫–∞ callback:
                # 1. –ë—ã–ª–æ —Å–æ–±—ã—Ç–∏–µ –∏ –ø—Ä–æ—à–ª–∞ –∑–∞–¥–µ—Ä–∂–∫–∞ event_delay
                # 2. –ò–ª–∏ –ø—Ä–æ—à—ë–ª –∏–Ω—Ç–µ—Ä–≤–∞–ª forced_timeout —Å –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∑–∞–ø—É—Å–∫–∞
                event_ready = (
                        self.event_detected and
                        (current_time - self.last_event_time >= self.event_delay)
                )

                timeout_ready = (
                        not self.event_detected and
                        current_time - last_callback_time >= self.forced_timeout
                )

                if event_ready or timeout_ready:
                    if event_ready:
                        logger.info("‚ñ∂Ô∏è –û–±—Ä–∞–±–æ—Ç–∫–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –ø–æ —Å–æ–±—ã—Ç–∏—é")
                    elif timeout_ready:
                        logger.info("üïí –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –ø–æ —Ç–∞–π–º–µ—Ä—É")

                    self.is_processing = True
                    try:
                        # –í—ã–ø–æ–ª–Ω—è–µ–º callback –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–π
                        self.callback()
                    except Exception as e:
                        # –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫–∏ callback, —á—Ç–æ–±—ã –æ–Ω–∏ –Ω–µ –ø—Ä–µ—Ä—ã–≤–∞–ª–∏ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥
                        logger.error(f"‚õî –û—à–∏–±–∫–∞ –≤ callback: {e}\n{traceback.format_exc()}")
                    finally:
                        # –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Ñ–ª–∞–≥–∏ –∏ –æ–±–Ω–æ–≤–ª—è–µ–º –≤—Ä–µ–º—è –æ–±—Ä–∞–±–æ—Ç–∫–∏
                        self.event_detected = False
                        self.is_processing = False
                        last_callback_time = current_time

        except Exception as e:
            logger.error(f"‚õî –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –≤ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–µ: {e}\n{traceback.format_exc()}")
